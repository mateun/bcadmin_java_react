package ttech.com.bitcoinapp.models;

public class BCTransaction {
	
	int id;
	String krakenId;
	float value;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getKrakenId() {
		return krakenId;
	}
	public void setKrakenId(String krakenId) {
		this.krakenId = krakenId;
	}
	public float getValue() {
		return value;
	}
	public void setValue(float value) {
		this.value = value;
	}
		
	
	
	

}
